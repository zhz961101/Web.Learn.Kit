#include <iostream>
using namespace std;

int main()
{
        int n  = 0;
        switch  (n)  {
        case   0:  do   {cout <<   " 0 "   <<  endl;
                 case 1:         cout <<   " 1 "   <<  endl;
                 case 2:         cout <<   " 2 "   <<  endl;
                 case   3:         cout <<   " 3 "   <<  endl;}   while ( --n  > 0 );
        }
}
